#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*res;
	int		i;
	size_t	mem;

	if (!s)
		return (0);
	i = 0;
	mem = ft_strlen(s);
	if (!(res = (char *)malloc(sizeof(*res) * mem + 1)))
		return (0);
	while (s[i] != '\0')
	{
		res[i] = f(s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
