#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;

	if (nb < 1)
		return (0);
	i = 1;
	while (i * i < nb)
	{
		i++;
	}
	if (nb % i == 0)
		return (i);
	else
		return (0);
}
